package com.sse.structuredencryption.models;

public class Token {

    int a, b;
    String s;

    public Token() {

    }

    public int getA() {
        return a;
    }

    public Token setA(int a) {
        this.a = a;
        return this;
    }

    public int getB() {
        return b;
    }

    public Token setB(int b) {
        this.b = b;
        return this;
    }

    public String getS() {
        return s;
    }

    public Token setS(String s) {
        this.s = s;
        return this;
    }
}
